package io.github.vinayreddykalluri.interviewhandbook.codingfoundations.javafundamentals;

public class ArrayPerspective {
    public static void main(String[] args) {
        int[] nums = {4, 5, 6, 7};
        int sum = 0;
        for (int n : nums) sum += n;
        System.out.println(sum);
    }
}
